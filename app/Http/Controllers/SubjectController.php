    /**
     * Store a newly created resource in storage.
     */
    public function store(Request $request)
    {
        $request->validate([
            'name' => ['required', 'string', 'min:1', 'max:255'],
            'email' => ['required', 'string', 'min:1', 'max:255', 'exists:teachers,email'],
        ]);

        $teacher = Teacher::where('email', $request->email)->first(); // Teacher retrieved

        $subject = new Subject(); // new Subject record
        $data = $request->except(['_token', '_method']); // move needless fields
        $subject->fill($data)->save(); // save record

        // Record in pivote table
        if(isset($teacher) > 0){
            $teacher->subjects()->attach($subject->id);
        }

        return redirect()->route('subjects.index');
    }

